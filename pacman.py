def square(x, y):
    """Draw square using path at coordinates x, y."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(3, 'white')


def move_ghost_smart(ghost):
    """Make ghosts choose a direction that gets closer to Pacman."""
    point, course = ghost
    options = [
        vector(10, 0),
        vector(-10, 0),
        vector(0, 10),
        vector(0, -10),
    ]

    valid_options = []

    for option in options:
        if valid(point + option):
            valid_options.append(option)

    if valid_options:
        best = valid_options[0]
        best_distance = abs((point + best).x - pacman.x) + abs((point + best).y - pacman.y)

        for option in valid_options:
            distance = abs((point + option).x - pacman.x) + abs((point + option).y - pacman.y)

            if distance < best_distance:
                best = option
                best_distance = distance

        ghost[1] = best
    else:
        ghost[1] = choice(options)


def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for ghost in ghosts:
        point, course = ghost

        if valid(point + course):
            point.move(course)
        else:
            move_ghost_smart(ghost)

        move_ghost_smart(ghost)

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 90)


def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


